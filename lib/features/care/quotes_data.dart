/// Returns the quote for the current 12-hour slot.
String currentLoveYourselfQuote([DateTime? now]) {
  final ms = (now ?? DateTime.now()).millisecondsSinceEpoch;
  const slotMs = 12 * 60 * 60 * 1000;
  final slot = ms ~/ slotMs;
  final index = _seededIndex(slot, _loveYourselfQuotes.length);
  final q = _loveYourselfQuotes[index];
  return S.isFusha ? q.fusha : q.masri;
}

// Matches java.util.Random's first nextInt(bound) for a given seed.
int _seededIndex(int slot, int bound) {
  var seed = (slot ^ 0x5DEECE66D) & ((1 << 48) - 1);
  int next(int bits) {
    seed = (seed * 0x5DEECE66D + 0xB) & ((1 << 48) - 1);
    return seed >> (48 - bits);
  }
  if ((bound & -bound) == bound) {
    return (bound * next(31)) >> 31;
  }
  int bits, val;
  do {
    bits = next(31);
    val = bits % bound;
  } while (bits - val + (bound - 1) < 0);
  return val;
}
