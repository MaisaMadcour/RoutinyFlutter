CareArticle careArticleFor(String key) => careArticles[key] ?? careDefaultArticle;

/// Finds the card (title + image) for a given article key, searching all
/// care sections. Used by the reflection result to open a matching article.
CareCardDef? careCardForArticle(String key) {
  for (final section in careSections) {
    for (final card in section.cards) {
      if (card.articleKey == key) return card;
    }
  }
  return null;
}
